package Data_Structures.Hash;

public class Element {
    // Field key
    public String key;
    // Field element
    public String elem;
    /**
     * Element Constructure
     * @param k String
     * @param v String
     */
    public Element( String k, String v ){
        // Set key
        this.key = k;
        // Set element
        this.elem = v;
    }
}
